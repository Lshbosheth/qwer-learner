// 处理函数：为词条添加 speakAs
function processDict(filePath) {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'))
  let count = 0

  const result = data.map((item) => {
    const name = item.name

    // 1. 精确匹配映射表
    if (speakAsMap[name]) {
      count++
      return { ...item, speakAs: speakAsMap[name] }
    }

    // 2. 纯大写字母缩写（2个字母以上，不含空格和连字符）
    if (/^[A-Z]{2,}$/.test(name)) {
      count++
      return { ...item, speakAs: name.split('').join(' ') }
    }

    // 3. 大写字母缩写 + 普通单词组合（如 "Gated RNN", "KL Divergence"）
    const hasAcronym = /\b[A-Z]{2,}\b/.test(name)
    if (hasAcronym && name !== name.toUpperCase()) {
      const processed = name.replace(/\b([A-Z]{2,})\b/g, (match) => match.split('').join(' '))
      if (processed !== name) {
        count++
        return { ...item, speakAs: processed }
      }
    }

    // 4. 含有特殊字符需要清理的（如逗号、括号、连字符等）
    if (/[(),]|-/.test(name) && !speakAsMap[name]) {
      let processed = name
        .replace(/[(),]/g, ' ')       // 移除括号和逗号
        .replace(/-/g, ' ')           // 连字符变空格
        .replace(/\b([A-Z]{2,})\b/g, (m) => m.split('').join(' '))  // 展开缩写
        .replace(/\s+/g, ' ')         // 合并多余空格
        .trim()
      if (processed !== name) {
        count++
        return { ...item, speakAs: processed }
      }
    }

    // 5. 包含 "K-" "N-" "V-" 等单字母前缀的术语
    if (/^[A-Z]-/.test(name) && !speakAsMap[name]) {
      const processed = name.replace(/^([A-Z])-/, '$1 ').replace(/-/g, ' ').replace(/\s+/g, ' ').trim()
      count++
      return { ...item, speakAs: processed }
    }

    return item
  })

  fs.writeFileSync(filePath, JSON.stringify(result, null, 2), 'utf-8')
  console.log(`处理完成: ${filePath}`)
  console.log(`  总词条: ${data.length}`)
  console.log(`  添加 speakAs: ${count}`)
}

